/*!
****************************************************************************************************
* 功能描述：该方法用于初始化驱动管理器模块
* 注意事项：NA
* 输入参数：NA
* 输出参数：NA
* 返回参数：NA
****************************************************************************************************
*/
void DRVMGR_Init(void)
{
	DRVMGR_CPUInit();
	DRVMGR_TimerInit(); // 其他模块依赖该模块，必须先初始化
	DRVMGR_LEDInit();
	DRVMGR_DIOInit();
	DRVMGR_SPIInit();

	DRVMGR_ADCInit();	// 该模块依赖SPI模块，需要后初始化
	DRVMGR_DACInit();	// 该模块依赖SPI模块，需要后初始化

	DRVMGR_UARTInit();
	DRVMGR_CANInit();
	DRVMGR_DIOPutOut();
}

/*!
****************************************************************************************************
* 功能描述：该方法是驱动管理器模块的周期服务函数
* 注意事项：需要在主循环中周期调用
* 输入参数：NA
* 输出参数：NA
* 返回参数：NA
****************************************************************************************************
*/
void DRVMGR_Handle(void)
{
	DRVMGR_CPUHandle();
	DRVMGR_TimerHandle();
	DRVMGR_LEDHandle();
	DRVMGR_DIOHandle();
	DRVMGR_SPIHandle();

	DRVMGR_ADCHandle();
	DRVMGR_DACHandle();
	DRVMGR_UARTHandle();
}

